def get_questions_by_mode(mode: str, category: str | None = None, level: str | None = None, limit: int = 5):
    """Получить вопросы по режиму, опционально фильтруя по категории и уровню."""
    import random

    if mode == "find_bug":
        pool = FIND_BUG_QUESTIONS
    elif mode == "guess_output":
        pool = GUESS_OUTPUT_QUESTIONS
    elif mode == "speed_code":
        pool = SPEED_CODE_QUESTIONS
    else:
        return []

    if category and category != "all":
        if category == "web":
            pool = [q for q in pool if q["category"] in ("javascript", "html_css")]
        else:
            pool = [q for q in pool if q["category"] == category]
    if level and level != "all":
        pool = [q for q in pool if q["level"] == level]

    if len(pool) <= limit:
        result = pool[:]
    else:
        result = random.sample(pool, limit)

    return result
