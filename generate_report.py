# ───────────────────────── helpers ──────────────────────────

def _set_cell_shading(cell, color_hex: str):
    shading = OxmlElement("w:shd")
    shading.set(qn("w:val"), "clear")
    shading.set(qn("w:color"), "auto")
    shading.set(qn("w:fill"), color_hex)
    cell._tc.get_or_add_tcPr().append(shading)


def _setup_document(doc: Document):
    """Настройка полей, шрифта по умолчанию, межстрочного интервала."""
    section = doc.sections[0]
    section.top_margin = Cm(2)
    section.bottom_margin = Cm(2)
    section.left_margin = Cm(3)
    section.right_margin = Cm(1.5)

    style = doc.styles["Normal"]
    font = style.font
    font.name = "Times New Roman"
    font.size = Pt(14)
    rFonts = style.element.rPr.rFonts if style.element.rPr is not None else None
    if rFonts is not None:
        rFonts.set(qn("w:eastAsia"), "Times New Roman")

    pf = style.paragraph_format
    pf.space_after = Pt(0)
    pf.space_before = Pt(0)
    pf.line_spacing = 1.5
    pf.first_line_indent = Cm(1.25)


def _add_empty_lines(doc, count=1):
    for _ in range(count):
        p = doc.add_paragraph()
        p.paragraph_format.first_line_indent = Cm(0)


def _add_centered(doc, text, size=14, bold=False):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.first_line_indent = Cm(0)
    run = p.add_run(text)
    run.font.name = "Times New Roman"
    run.font.size = Pt(size)
    run.bold = bold
    return p


def _add_normal(doc, text, bold=False, italic=False, indent=True):
    p = doc.add_paragraph()
    if not indent:
        p.paragraph_format.first_line_indent = Cm(0)
    run = p.add_run(text)
    run.font.name = "Times New Roman"
    run.font.size = Pt(14)
    run.bold = bold
    run.italic = italic
    return p


def _add_heading1(doc, text):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.first_line_indent = Cm(0)
    p.paragraph_format.space_before = Pt(12)
    p.paragraph_format.space_after = Pt(12)
    run = p.add_run(text.upper())
    run.font.name = "Times New Roman"
    run.font.size = Pt(14)
    run.bold = True
    return p


def _add_heading2(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.first_line_indent = Cm(0)
    p.paragraph_format.space_before = Pt(6)
    p.paragraph_format.space_after = Pt(6)
    run = p.add_run(text)
    run.font.name = "Times New Roman"
    run.font.size = Pt(14)
    run.bold = True
    return p


def _add_figure_placeholder(doc, number, caption):
    _add_empty_lines(doc, 2)
    _add_centered(doc, f"[Вставьте скриншот здесь]", size=12, bold=False)
    _add_empty_lines(doc, 2)
    cap = doc.add_paragraph()
    cap.alignment = WD_ALIGN_PARAGRAPH.CENTER
    cap.paragraph_format.first_line_indent = Cm(0)
    cap.paragraph_format.space_after = Pt(12)
    run = cap.add_run(f"Рисунок {number} — {caption}")
    run.font.name = "Times New Roman"
    run.font.size = Pt(14)
    run.italic = True


def _add_code_location(doc, filepath, lines):
    p = doc.add_paragraph()
    p.paragraph_format.first_line_indent = Cm(0)
    p.paragraph_format.space_before = Pt(6)
    run = p.add_run(f"Расположение кода: {filepath} (строки {lines})")
    run.font.name = "Times New Roman"
    run.font.size = Pt(12)
    run.italic = True


